
public class operacoes {

	public void soma ( int x1, int x2) {
	
	int resultado = x1 +x2;
	System.out.println("O resultado da sua soma é " + resultado);

	}
	public void multiplicacao ( int x1, int x2) {
		int resultado = x1*x2;
		System.out.println("O resultado da sua multiplicacao é " + resultado);
	}
	public void divisao ( int x1, int x2) {
		int resultado = x1/x2;
		System.out.println("O resultado da sua divisao é " + resultado);
	}
	public void subtracao ( int x1, int x2) {
		int resultado = x1-x2;
		System.out.println("O resultado da sua subtracao é " + resultado);
	}
}


